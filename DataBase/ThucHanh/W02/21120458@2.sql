	CONSTRAINT FK_THAMGIADT_GIAOVIEN FOREIGN KEY (MAGV)REFERENCES GIAOVIEN (MAGV),
	CONSTRAINT FK_THAMGIADT_CONGVIEC FOREIGN KEY (MADT, STT)REFERENCES CONGVIEC(MADT, SOTT)

