CREATE TABLE KHOA 
(
	MAKHOA char(10), 
	TENKHOA nvarchar(40), 
	NAMTL int, 
	PHONG char(10), 
	DIENTHOAI char(12), 	
	TRUONGKHOA char(10), 
	NGAYNHANCHUC date,

	CONSTRAINT PK_KHOA
	PRIMARY KEY (MAKHOA)	
)


CREATE TABLE BOMON 
(
	MABM nchar(10),
	TENBM nvarchar(40), 	 
	PHONG char(10),
	DIENTHOAI char(12), 
	TRUONGBM char(10), 
	MAKHOA char(10), 	
	NGAYNHANCHUC date,

	CONSTRAINT PK_BOMON
	PRIMARY KEY (MABM)
)


CREATE TABLE GV_DT 
(
	MAGV char(10), 
	DIENTHOAI char(12),

	CONSTRAINT PK_DIENTHOAI
	PRIMARY KEY (MAGV, DIENTHOAI)
)


CREATE TABLE DETAI 
(
	MADT char(10), 
	TENDT nvarchar(100), 
	KINHPHI float, 
	CAPQL nvarchar(40), 
	NGAYBD date, 
	NGAYKT date, 	
	MACD nchar(10),
	GVCNDT char(10), 	

	CONSTRAINT PK_DETAI
	PRIMARY KEY (MADT)
)

CREATE TABLE CHUDE 
(
	MACD nchar(10), 
	TENCD nvarchar(50),

	CONSTRAINT PK_CHUDE
	PRIMARY KEY (MACD)
)


CREATE TABLE CONGVIEC 
(
	MADT char(10), 
	SOTT int, 
	TENCV nvarchar(40), 
	NGAYBD date, 
	NGAYKT date,

	CONSTRAINT PK_CONGVIEC
	PRIMARY KEY (MADT, SOTT) 
)


CREATE TABLE THAMGIADT
(
	MAGV char(10), 
	MADT char(10), 
	STT int, 
	PHUCAP float, 
	KETQUA nvarchar(40),

	CONSTRAINT PK_PHANCONG
	PRIMARY KEY (MAGV, MADT, STT)
)

CREATE TABLE NGUOITHAN 
(
	MAGV char(10), 
	TEN nvarchar(40), 
	NGSINH date, 
	PHAI nchar(3),

	CONSTRAINT PK_NGUOITHAN
	PRIMARY KEY (MAGV, TEN)
)


