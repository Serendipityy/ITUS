	CONSTRAINT FK_DETAI_CHUDE FOREIGN KEY (MACD)REFERENCES CHUDE (MACD),
	CONSTRAINT FK_DETAI_GIAOVIEN FOREIGN KEY (GVCNDT) REFERENCES GIAOVIEN (MAGV)

